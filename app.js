function populateMatches(team) {
  const grid = document.getElementById("match-grid");
  grid.innerHTML = "";
  ipl2025Matches.forEach((match, index) => {
    if (match.team1 === team || match.team2 === team || team === "") {
      const matchCard = document.createElement("div");
      matchCard.classList.add("match-card");
      matchCard.innerHTML = `
      <p class="match-number">MATCH ${index + 1}</p>
      <div class="info">
      <p class="venue">${match.venue}</p>
      <p class="date">${new Date(match.date).toLocaleDateString("en-US", {
        day: "2-digit",
        month: "short",
        year: "numeric",
      })}</p>
      </div>
      <div class="teams">
        <p class="team1">${match.team1}</p>
        <p class="vs">V/S</p>
        <p class="team2">${match.team2}</p>
      </div>
      <div class="winner">
        <p class="w-team">${match.winner || '<span style="color : grey; font-style : italic;">TBD</span>'}</p>
      </div>`;
      grid.appendChild(matchCard);
    }
  });
}

populateMatches("");

const selectedTeam = document.getElementById("team");
selectedTeam.addEventListener("change", () => {
  populateMatches(selectedTeam.value);
});

